temperature = 28
is_sunny = True

# and 연산자: 모든 조건이 참일 때 참
if temperature > 25 and is_sunny:
    print("더운 날씨입니다. 선크림을 바르세요.")

# or 연산자: 하나라도 참이면 참
if temperature < 10 or temperature > 30:
    print("실내에 있는 것이 좋겠습니다.")

# not 연산자: 조건의 반대
if not is_sunny:
    print("흐린 날씨입니다.")
else:
    print("맑은 날씨입니다.")

