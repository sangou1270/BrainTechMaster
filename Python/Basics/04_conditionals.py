# ==========================================================
print("\n=== if-else 문 ===")
y = -5

if y > 0:
    print("y는 양수입니다.")
else:
    print("y는 0 또는 음수입니다.")

# ==========================================================
# if-elif-else 문
# ==========================================================
print("\n=== if-elif-else 문 ===")
score = 85

if score >= 90:
    grade = "A"
elif score >= 80:
    grade = "B"
elif score >= 70:
    grade = "C"
elif score >= 60:
    grade = "D"
else:
    grade = "F"

print(f"점수: {score}, 등급: {grade}")

