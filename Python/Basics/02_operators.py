a = 10
b = 3

# 기본 산술 연산을 출력해봅시다
print("=== 산술 연산자 ===")
print("a =", a, "b =", b)
print("a + b =", a + b)  # 덧셈
print("a - b =", a - b)  # 뺄셈
print("a * b =", a * b)  # 곱셈
print("a / b =", a / b)  # 나눗셈 (실수 결과)
print("a // b =", a // b)  # 몫 (정수 나눗셈)
print("a % b =", a % b)  # 나머지
print("a ** b =", a ** b)  # 거듭제곱

# ==========================================================
# 비교 연산자
# ==========================================================
